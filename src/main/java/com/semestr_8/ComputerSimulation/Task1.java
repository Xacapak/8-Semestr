package com.semestr_8.ComputerSimulation;

import java.util.ArrayList;
import java.util.Random;

/**
 * Задание 1: обеспечить определение на числовом отрезке [0,100] случайное положение точек – моментов событий нестационарного пуассоновского потока
 *  с заданной функцией λ(t)=0,2-0,003t, представляющей собой интенсивность потока. Дать графическую иллюстрацию.
 */
public class Task1 {

    // ---------------------------------------- КОНСТАНТЫ ----------------------------------------

    private static final double T_MAX = 100.0;                  // Конец временного интервала
    private static final double LAMBDA_MAX = 0.2;               // Максимальная интенсивность потока
    private final Random random;                                // Генератор случайных чисел (0, 1)
    private static final double T_ZERO = 0.2 / 0.003;           // Момент времени, когда интенсивность равна 0

    // ---------------------------------------- КОНСТРУКТОР ----------------------------------------

    /**
     * Конструктор инициализирует генератор случайных чисел.
     */
    public Task1(){
        this.random = new Random();
    }

    // ---------------------------------------- МЕТОДЫ ----------------------------------------

    /**
     * Вычисляет интенсивность потока в заданный момент времени.
     *
     * @param t момент времени
     * @return интенсивность λ(t) (неотрицательная)
     * <p>
     * Теория: λ(t) = 0.2 - 0.003t, но не может быть отрицательной.
     * Math.max гарантирует, что после t > 66.67 интенсивность = 0.
     */
    private double lambda(double t){
        return Math.max(0.0, 0.2 - 0.003 * t);
    }

    /**
     * Вычисляет теоретическое среднее число событий на интервале [0, 100].
     *
     * @return среднее число событий (интеграл от λ(t) от 0 до T_ZERO)
     * <p>
     * Теория: среднее число событий = площадь под графиком λ(t).
     * Для линейной функции λ(t) = b - a*t интеграл равен b*t - (a/2)*t².
     * Интегрируем только до T_ZERO, т.к. После λ(t)=0.
     */
    private double calculateTheoreticalMean(){
        return 0.2 * T_ZERO - 0.0015 * T_ZERO * T_ZERO;
    }

    // ---------------------------------------- МЕТОД ОСНОВНОЙ ----------------------------------------

    public void run(){

        ArrayList<Double> events = new ArrayList<>();               // Список для хранения моментов событий (принятые)
        double currentTime = 0.0;                                   // Текущее время моделирования

        while (currentTime < T_MAX){

            // ШАГ 1: Генерация интервала до следующего кандидата
            double r1 = random.nextDouble();

            if (r1 == 0.0) r1 = 0.000001;                           // Защита от ln(0)

            // Формула интервала для пуассоновского потока
            double interval = -Math.log(r1) / LAMBDA_MAX;
            currentTime += interval;

            // Проверка интервала на выход за пределы
            if (currentTime > T_MAX){
                break;
            }

            // ШАГ 2: Вероятность принять кандидата
            double lambda_t = lambda(currentTime);                  // Реальная интенсивность в этот момент
            double p = lambda_t / LAMBDA_MAX;                       // Вероятность принять

            // ШАГ 3: Решение - принять или отвергнуть
            double r2 = random.nextDouble();

            if (r2 < p){
                events.add(currentTime);
            }
        }

        // ---------------------------------------- ВЫВОД РЕЗУЛЬТАТА ----------------------------------------

        System.out.println("Задание 1: Нестационарный пуассоновский поток");
        System.out.printf("λ(t) = 0.2 - 0.003t на [0, 100]%n");
        System.out.printf("Теоретическое среднее: %.2f событий%n", calculateTheoreticalMean());
        System.out.printf("Получено событий: %d%n", events.size());
        for (int i = 0; i < events.size(); i++) {
            System.out.printf("t%-2d = %6.2f\n", i + 1, events.get(i));
        }

        // ---------------------------------------- ВИЗУАЛИЗАЦИЯ ----------------------------------------

        GraphicalConstruction.plotTask1(events, LAMBDA_MAX, T_ZERO);

        // ---------------------------------------- ОЖИДАНИЕ ----------------------------------------

        System.out.println("\nНажмите Enter для возврата в меню...");
        new java.util.Scanner(System.in).nextLine();

    }
}
